    private void jCB_CaboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCB_CaboActionPerformed
        envia_Dado_Cabo();
        String resposta = this.maquinaInferencia.verifica_cabo();
        if(resposta != null){
            oculta_componentes_exceto_cabo();
            JOptionPane.showMessageDialog(null, resposta);
        }else{
            exibe_demais_opcoes();
        }
    }//GEN-LAST:event_jCB_CaboActionPerformed

