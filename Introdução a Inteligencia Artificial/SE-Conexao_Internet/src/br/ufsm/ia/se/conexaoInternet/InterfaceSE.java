        this.maquinaInferencia.getBaseConhecimento().setErro(jCB_Erro.getSelectedIndex());
        this.maquinaInferencia.getBaseConhecimento().setIp(jCB_IP.getSelectedIndex());
        this.maquinaInferencia.getBaseConhecimento().setLenta(jCB_Lenta.getSelectedIndex());
        this.maquinaInferencia.getBaseConhecimento().setNavegador(jCB_Navegador.getSelectedItem().toString());
        this.maquinaInferencia.getBaseConhecimento().setOutro(jCB_Outro.getSelectedIndex());
        this.maquinaInferencia.getBaseConhecimento().setProblema_IP(jCB_Problema_IP.getSelectedIndex());
        this.maquinaInferencia.getBaseConhecimento().setProxy(jCB_Proxy.getSelectedIndex());
